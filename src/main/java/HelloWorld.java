public class HelloWorld {
    public static void main(String[] args) {
        // 2 пункт
        int n = 5;
        double f = 5.5;
        System.out.println(n+f);
        System.out.println(n*f);
        System.out.println(n/f);
        System.out.println(n-f);
        // 1 пункт
        int a = Integer.MAX_VALUE;
        int b = Integer.MIN_VALUE;
        System.out.println(a-1);
        System.out.println(a+1);
        System.out.println(b+1);
        System.out.println(b-1);
        System.out.println(n>=f);
        System.out.println(n==f);
        System.out.println(n!=f);
        System.out.println(n<=f);
    }
}
